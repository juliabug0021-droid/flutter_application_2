import 'package:flutter/material.dart';

class HomeworkBlocScreen extends StatefulWidget {
  final String title;

  const HomeworkBlocScreen({super.key, required this.title});

  @override
  State<HomeworkBlocScreen> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<HomeworkBlocScreen> {
  int _counter = 0;

  void _incrementCounter() {
    setState(() {
      _counter++;
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: Text(widget.title)),
      body: Center(
        child: Column(
          mainAxisAlignment: .center,
          children: [
            const Text('You have pushed the button this many times:'),
            Text(
              '$_counter',
              style: Theme.of(context).textTheme.headlineMedium,
            ),
          ],
        ),
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _incrementCounter,
        tooltip: 'Increment',
        child: const Icon(Icons.add),
      ),
    );
  }
}
